                <span>Санкт-Петербург, Россия</span>
                <p>Не каждый день встречаешь человека с опытом практики йоги 35 лет. Константин начал заниматься йогой еще в советские годы, когда никакой качественной литературы и курсов еще не было. Сейчас уже у него собственная школа Йоги, где обучают качественных тренеров и практиков йоги. Констатин Харьковский Занимается йогой с 1979 года, преподает с 1999 года. Руководитель и основатель Петербургской школы йоги «Поток странствий» (с 1997 года). Разработчик учебных программ для практиков йоги различного уровня. Инициатор создания общественной организации — Объединения инструкторов йоги Санкт-Петербурга «Поток странствий», преобразованного впоследствии в Петербургскую Школу йоги «Поток странствий». Преподаватель «Школы инструкторов йоги». Учредитель и главный редактор первого петербургского журнала о йоге «ёGa в потоке странствий».</p>
                <p>С 1997 года на практике реализует идею популяризации йоги в Петербурге, России и мире. Принимал участие в нескольких десятках наиболее значимых семинаров ведущих отечественных и зарубежных мастеров йоги. Организовал несколько десятков семинаров Мастеров йоги в Петербурге. Автор и разработчик учебно-методических курсов «YogaFlow System», «Основы пранаямы», «Константы йоги», ретритной программы «Йога 24 часа в сутки». Один из основателей нового направления в йоге – «Йогический туризм» (с 1999 года). Константин объездил уже весь мир. Имеет опыт практики боевых искусств: айкидо, яйдо. Квалифицирован Тэкимури-сенсэем на 1-й дан айкидо в стиле Нишио-сенсэя в Швеции, Упсала, 2001 год.</p>
